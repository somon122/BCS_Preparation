package com.world_tech_point.bcs_preparation.ArchiveSection;

public class ArchiveClass {

    private int archiveId;
    private String archiveSubject;
    private String archiveCategory;
    private String archiveDate;

    public ArchiveClass() {
    }

    public int getArchiveId() {
        return archiveId;
    }

    public void setArchiveId(int archiveId) {
        this.archiveId = archiveId;
    }

    public String getArchiveSubject() {
        return archiveSubject;
    }

    public void setArchiveSubject(String archiveSubject) {
        this.archiveSubject = archiveSubject;
    }

    public String getArchiveCategory() {
        return archiveCategory;
    }

    public void setArchiveCategory(String archiveCategory) {
        this.archiveCategory = archiveCategory;
    }

    public String getArchiveDate() {
        return archiveDate;
    }

    public void setArchiveDate(String archiveDate) {
        this.archiveDate = archiveDate;
    }
}
